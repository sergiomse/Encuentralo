    public static String nearestMaterialColor(int source) {
        String minDistColor = null;
        double minDist = Double.MAX_VALUE;
        for(Map.Entry<String, Integer> color : colors.entrySet()) {
            int compColor = color.getValue();
            double dist = Math.sqrt( Math.pow(Color.red(compColor) - Color.red(source), 2) +
                                    Math.pow(Color.green(compColor) - Color.green(source), 2) +
                                    Math.pow(Color.blue(compColor) - Color.blue(source), 2));
            if(dist < minDist) {
                minDist = dist;
                minDistColor = color.getKey();
            }
        }

        return minDistColor;
    }

